    private void popuniTabelu() {
        try {
            StavkaIznajmljivanja kriterijum = new StavkaIznajmljivanja();
            kriterijum.setIznajmljivanje(iznajmljivanje);

            Request zahtev = new Request();
            zahtev.setData(kriterijum);
            zahtev.setOperation(Operacije.GET_ALL_STAVKA_IZNAJMLJIVANJA);

            Komunikacija.getInstance().posaljiZahtev(zahtev);
            Response odgovor = Komunikacija.getInstance().primiOdgovor();

            ArrayList<StavkaIznajmljivanja> stavke = (ArrayList<StavkaIznajmljivanja>) odgovor.getResult();
            tblStavke.setModel(new ModelTabeleStavkaIznajmljivanja(stavke));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Greska prilikom ucitavanja stavki iznajmljivanja!", "Greska!", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    
