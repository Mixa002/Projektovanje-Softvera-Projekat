    private void popuniAutomobile() {
        try {
            Request zahtev = new Request();
            zahtev.setOperation(Operacije.GET_ALL_AUTOMOBIL);
            Komunikacija.getInstance().posaljiZahtev(zahtev);
            Response odgovor = Komunikacija.getInstance().primiOdgovor();

            ArrayList<Automobil> listaAutomobila = (ArrayList<Automobil>) odgovor.getResult();
            cmbAutomobil.removeAllItems();

            for (Automobil automobil : listaAutomobila) {
                cmbAutomobil.addItem(automobil);
                if (automobil.getAutomobilID() == stavka.getAutomobi().getAutomobilID()) {
                    cmbAutomobil.setSelectedItem(automobil);
                }
            }
            txtDatumOd.setText(new SimpleDateFormat("dd.MM.yyyy").format(stavka.getDatumOd()));
            txtDatumDo.setText(new SimpleDateFormat("dd.MM.yyyy").format(stavka.getDatumDo()));
            txtNapomena.setText(stavka.getNapomena());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Greska prilikom popunjavanja automobila!", "Greska!", JOptionPane.ERROR_MESSAGE);
        }
    }
