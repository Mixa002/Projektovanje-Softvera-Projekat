    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        try {
            Automobil a = (Automobil) cmbAutomobil.getSelectedItem();

            if (txtDatumOd.getText().isEmpty() || txtDatumDo.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Morate uneti oba datuma!");
                return;
            }

            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
            Date datumOd = sdf.parse(txtDatumOd.getText());
            Date datumDo = sdf.parse(txtDatumDo.getText());
            String napomena = txtNapomena.getText();

            if (datumDo.before(datumOd)) {
                JOptionPane.showMessageDialog(this, "Datum do mora biti posle datuma od!", "Greska!", JOptionPane.ERROR_MESSAGE);
                return;
            }

            ParametarDostupnihAutomobila parametar = new ParametarDostupnihAutomobila(datumOd, datumDo);
            Request zahtevProvera = new Request();
            zahtevProvera.setData(parametar);
            zahtevProvera.setOperation(Operacije.GET_ALL_DOSTUPNE_AUTOMOBILE);
            Komunikacija.getInstance().posaljiZahtev(zahtevProvera);
            Response odgovorProvera = Komunikacija.getInstance().primiOdgovor();
            ArrayList<Automobil> dostupniAutomobili = (ArrayList<Automobil>) odgovorProvera.getResult();

//            System.out.println("Dostupni automobili u izabranom periodu:");
//            for (Automobil auto : dostupniAutomobili) {
//                System.out.println(auto.getAutomobilID() + " - " + auto.getMarka() + " " + auto.getModel() + ", cena: " + auto.getCenaPoDanu());
//            }
            if (!dostupniAutomobili.contains(a)) {
                if (a.getAutomobilID() == stavka.getAutomobi().getAutomobilID()) {
                    JOptionPane.showMessageDialog(this,
                            "Ako želite da promenite datum za ovaj auto u periodu koji se preklapa sa trenutnim, "
                            + "moraćete prvo da obrišete ovo iznajmljivanje!",
                            "Greska!", JOptionPane.ERROR_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this,
                            "Automobil je zauzet u izabranom periodu!",
                            "Greska!", JOptionPane.ERROR_MESSAGE);
                }
                return;
            }

            stavka.setAutomobi(a);
            stavka.setDatumOd(datumOd);
            stavka.setDatumDo(datumDo);
            stavka.setBrojDana((int) ((datumDo.getTime() - datumOd.getTime()) / (1000 * 60 * 60 * 24)));
            stavka.setCena(stavka.getBrojDana() * a.getCenaPoDanu());
            stavka.setNapomena(napomena);

            Request zahtev1 = new Request();
            zahtev1.setData(stavka);
            zahtev1.setOperation(Operacije.UPDATE_STAVKA_IZNAJMLJIVANJA);
            Komunikacija.getInstance().posaljiZahtev(zahtev1);
            Komunikacija.getInstance().primiOdgovor();

            Request zahtevSveStavke = new Request();
            StavkaIznajmljivanja kriterijum = new StavkaIznajmljivanja();
            kriterijum.setIznajmljivanje(stavka.getIznajmljivanje());
            zahtevSveStavke.setData(kriterijum);
            zahtevSveStavke.setOperation(Operacije.GET_ALL_STAVKA_IZNAJMLJIVANJA);
            Komunikacija.getInstance().posaljiZahtev(zahtevSveStavke);
            Response odgovorSveStavke = Komunikacija.getInstance().primiOdgovor();
            ArrayList<StavkaIznajmljivanja> stavke = (ArrayList<StavkaIznajmljivanja>) odgovorSveStavke.getResult();

            double ukupno = 0;
            for (StavkaIznajmljivanja si : stavke) {
                ukupno += si.getCena();
            }

            System.out.println("Izracunato ukupno: " + ukupno);

//            Iznajmljivanje iz = stavka.getIznajmljivanje();
//            iz.setUkupanIznos(ukupno);
            Iznajmljivanje zaSlanje = new Iznajmljivanje();
            zaSlanje.setIznajmljivanjeID(stavka.getIznajmljivanje().getIznajmljivanjeID());
            zaSlanje.setUkupanIznos(ukupno);

            Request zahtev2 = new Request();
            zahtev2.setData(zaSlanje);
            System.out.println("Pripremljeno za slanje: " + zaSlanje.getUkupanIznos());
            zahtev2.setOperation(Operacije.UPDATE_IZNAJMLJIVANJE);
            Komunikacija.getInstance().posaljiZahtev(zahtev2);
            Komunikacija.getInstance().primiOdgovor();

            JOptionPane.showMessageDialog(this, "Iznajmljivanje uspesno promenjeno");

            for (java.awt.Window window : java.awt.Window.getWindows()) {
                if (window instanceof javax.swing.JFrame && !(window instanceof KlijentskaForma)) {
                    window.dispose();
                }
            }

        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Greska prilikom izmene stavke!", "Greska!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSacuvajActionPerformed

