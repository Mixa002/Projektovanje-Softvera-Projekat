    private boolean datumiSePreklapaju(Date pocetak1, Date kraj1, Date pocetak2, Date kraj2) {
        return !(kraj1.before(pocetak2) || kraj2.before(pocetak1));
    }

    private void popuniDostupneAutomobile() {

        System.out.println("popunjavam automobile!");
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        try {
            String datumOdText = txtDatumOd.getText().trim();
            String datumDoText = txtDatumDo.getText().trim();

            if (datumOdText.isEmpty() || datumDoText.isEmpty()) {
                return;
            }

            Date datumOd = sdf.parse(datumOdText);
            Date datumDo = sdf.parse(datumDoText);

            if (datumOd.after(datumDo)) {
                return;
            }

            ParametarDostupnihAutomobila parametar = new ParametarDostupnihAutomobila(datumOd, datumDo);
            Request zahtev = new Request();
            zahtev.setOperation(Operacije.GET_ALL_DOSTUPNE_AUTOMOBILE);
            zahtev.setData(parametar);

            Komunikacija.getInstance().posaljiZahtev(zahtev);
            Response odgovor = Komunikacija.getInstance().primiOdgovor();

            if (odgovor.getException() != null) {
                return;
            }

            ArrayList<Automobil> dostupni = (ArrayList<Automobil>) odgovor.getResult();
            cmbAutomobil.removeAllItems();
            for (Automobil a : dostupni) {
                cmbAutomobil.addItem(a);
            }

            trenutnoDostupniAutomobili.clear();
            trenutnoDostupniAutomobili.addAll(dostupni);

        } catch (ParseException | ClassCastException ex) {
            // format nije validan, ignoriši

        }
        cmbAutomobil.repaint();
        cmbAutomobil.revalidate();
    }
