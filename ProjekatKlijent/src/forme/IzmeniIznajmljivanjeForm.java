    private void btnObrisiStavkuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiStavkuActionPerformed
        int selectedRow = tblStavkaIznajmljivanja.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Morate izabrati stavku za brisanje!", "Greška!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        ModelTabeleStavkaIznajmljivanja mt = (ModelTabeleStavkaIznajmljivanja) tblStavkaIznajmljivanja.getModel();
        StavkaIznajmljivanja obrisana = mt.getStavkaAt(selectedRow);
        mt.obrisiStavku(selectedRow);

        Automobil obrisanAuto = obrisana.getAutomobi();

        boolean postoji = false;
        for (Automobil a : trenutnoDostupniAutomobili) {
            if (a.getAutomobilID() == obrisanAuto.getAutomobilID()) {
                postoji = true;
                break;
            }
        }
        if (!postoji) {
            trenutnoDostupniAutomobili.add(obrisanAuto);
        }
        cmbAutomobil.removeAllItems();
        for (Automobil a : trenutnoDostupniAutomobili) {
            cmbAutomobil.addItem(a);
        }
        mt.fireTableDataChanged();


    }//GEN-LAST:event_btnObrisiStavkuActionPerformed

