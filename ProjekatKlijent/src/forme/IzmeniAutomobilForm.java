    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        String regBr = txtRegistarskiBroj.getText();
        String cena = txtCenaPoDanu.getText();
        if (regBr.isEmpty() || cena.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!", "Greska!", JOptionPane.ERROR_MESSAGE);
            return;
        }

        double cenaPoDanu;
        try {
            cenaPoDanu = Double.parseDouble(cena);
            if (cenaPoDanu <= 0) {
                JOptionPane.showMessageDialog(this, "Cena mora biti veca od nule!", "Greska!", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Cena mora biti broj!", "GreÅ¡ka", JOptionPane.ERROR_MESSAGE);
            return;
        }
        this.automobil.setRegistarskiBroj(regBr);
        this.automobil.setCenaPoDanu(cenaPoDanu);

        Request zahtev = new Request();
        zahtev.setData(automobil);
        zahtev.setOperation(Operacije.UPDATE_AUTOMOBIL);
        try {
            Komunikacija.getInstance().posaljiZahtev(zahtev);

            Response odgovor = Komunikacija.getInstance().primiOdgovor();

            if (odgovor.getStatus() == ResponseStatus.Success && (boolean) odgovor.getResult()) {
                izmenjeno = true;
                JOptionPane.showMessageDialog(this, "Uspesno izmenjen automobil!");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Izmena nije uspela!", "Greska!", JOptionPane.ERROR_MESSAGE);
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, "Greska prilikom izmene: " + ex.getMessage(), "Greska!", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnSacuvajActionPerformed

