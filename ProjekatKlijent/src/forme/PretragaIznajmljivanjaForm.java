    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        int selectedRow = tblIznajmljivanja.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Morate izabrati iznajmljivanje!", "Greska!", JOptionPane.ERROR_MESSAGE);
            return;
        }

        ModelTabeleIznajmljivanja mt = (ModelTabeleIznajmljivanja) tblIznajmljivanja.getModel();
        Iznajmljivanje iz = mt.getIznajmljivanje(selectedRow);

        int potvrda = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da obrisete ovo iznajmljivanje?", "Potvrda brisanja", JOptionPane.YES_NO_OPTION);

        if (potvrda != JOptionPane.YES_OPTION) {
            return;
        }

        try {
            Request zahtev = new Request();
            zahtev.setData(iz);
            zahtev.setOperation(Operacije.DELETE_IZNAJMLJIVANJE);

            Komunikacija.getInstance().posaljiZahtev(zahtev);

            Response odgovor = Komunikacija.getInstance().primiOdgovor();
            System.out.println(odgovor.getResult().getClass().getName());
            boolean uspesno = (boolean) odgovor.getResult();
            if (uspesno) {
                JOptionPane.showMessageDialog(this, "Iznajmljivanje je uspesno obrisano!");
                svaIznajmljivanja.removeIf(izn -> izn.getIznajmljivanjeID() == iz.getIznajmljivanjeID());
                filtrirajIznajmljivanja();
            } else {
                JOptionPane.showMessageDialog(this, "Brisanje nije uspelo!", "Gre≈°ka!", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Greska prilikom brisanja iznajmljivanja!", "Greska!", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnObrisiActionPerformed

