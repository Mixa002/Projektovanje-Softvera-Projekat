    private void ucitajIznajmljivanja() {
        try {

            Request zahtev = new Request();
            zahtev.setOperation(Operacije.GET_ALL_IZNAJMLJIVANJE);
            Komunikacija.getInstance().posaljiZahtev(zahtev);
            Response odgovor = Komunikacija.getInstance().primiOdgovor();
            svaIznajmljivanja = (ArrayList<Iznajmljivanje>) odgovor.getResult();
            ModelTabeleIznajmljivanja mt = new ModelTabeleIznajmljivanja(svaIznajmljivanja);
            tblIznajmljivanja.setModel(mt);
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Greska prilikom ucitavanja klijenata!", "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void filtrirajIznajmljivanja() {

        if (svaIznajmljivanja == null) {
            return;
        }
        String text = txtKlijent.getText().toLowerCase().trim();

        if (text.isEmpty()) {
            tblIznajmljivanja.setModel(new ModelTabeleIznajmljivanja(svaIznajmljivanja));
            return;
        }

        ArrayList<Iznajmljivanje> filtrirana = new ArrayList<>();

        for (Iznajmljivanje iznajmljivanje : svaIznajmljivanja) {
            String ime = iznajmljivanje.getKlijent().getIme() != null ? iznajmljivanje.getKlijent().getIme().toLowerCase() : "";
            String prezime = iznajmljivanje.getKlijent().getPrezime() != null ? iznajmljivanje.getKlijent().getPrezime().toLowerCase() : "";
            String punoIme = (ime + " " + prezime).trim();

            if (punoIme.contains(text)) {
                filtrirana.add(iznajmljivanje);
            }
        }
        ModelTabeleIznajmljivanja mt = new ModelTabeleIznajmljivanja(filtrirana);
        tblIznajmljivanja.setModel(mt);
    }
