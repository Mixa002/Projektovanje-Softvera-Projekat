    private void btnObrisiAutomobilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiAutomobilActionPerformed
        int selectedRow = tblAutomobili.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Izaberi automobil za brisanje!");
        }
        Automobil a = ((ModelTabeleAutomobili) tblAutomobili.getModel()).getAutomobilAt(selectedRow);

        int confirm = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da izbrisete ovaj automobil?", "Potvrda brisanja", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }
        try {
            Request zahtev = new Request();
            zahtev.setOperation(Operacije.DELETE_AUTOMOBIL);
            zahtev.setData(a);

            Komunikacija.getInstance().posaljiZahtev(zahtev);
            Response odgovor = Komunikacija.getInstance().primiOdgovor();

            if (odgovor.getStatus() == ResponseStatus.Success && odgovor.getResult() != null && (boolean) odgovor.getResult()) {
                JOptionPane.showMessageDialog(this, "Automobil uspešno obrisan.");
                sviAutomobili.remove(a); // uklanjamo i lokalno
                filtrirajAutomobile();   // osvežimo prikaz
            } else {
                String poruka = odgovor.getException() != null ? odgovor.getException().getMessage() : "Brisanje nije uspelo.";
                JOptionPane.showMessageDialog(this, poruka, "Greška", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Greska prilikom brisanja!", "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnObrisiAutomobilActionPerformed

