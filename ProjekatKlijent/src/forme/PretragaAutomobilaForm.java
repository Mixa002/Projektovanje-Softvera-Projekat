    private void ucitajTabelu() {
        try {
            Request zahtev = new Request();
            zahtev.setOperation(Operacije.GET_ALL_AUTOMOBIL);
            Komunikacija.getInstance().posaljiZahtev(zahtev);
            Response odgovor = Komunikacija.getInstance().primiOdgovor();
            sviAutomobili = (ArrayList<Automobil>) odgovor.getResult();

            filtrirajAutomobile();
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Greska prilikom ucitavanja automobila!", "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }

    public void filtrirajAutomobile() {
        if (sviAutomobili == null) {
            return;
        }
        ArrayList<Automobil> filtrirani = new ArrayList<>(sviAutomobili);

        if (cbMarkaAutomobila.isSelected()) {
            String marka = txtMarkaAutomobila.getText().trim().toLowerCase();
            filtrirani.removeIf(a -> !a.getMarka().toLowerCase().contains(marka));
        }

        if (cbCenaPoDanuAutomobila.isSelected()) {
            try {
                String cenaText = txtCenaPoDanuAutomobila.getText().trim();
                if (!cenaText.isEmpty()) {
                    double cena = Double.parseDouble(cenaText);
                    filtrirani.removeIf(a -> a.getCenaPoDanu() > cena);
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Unesite ispravnu cenu!", "Greska!", JOptionPane.WARNING_MESSAGE);
            }

        }
        tblAutomobili.setModel(new ModelTabeleAutomobili(filtrirani));

    }
