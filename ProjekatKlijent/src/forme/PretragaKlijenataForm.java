    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        int selectedRow = tblKlijenti.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Izaberite klijenta za brisanje!", "Greska!", JOptionPane.ERROR_MESSAGE);
            return;
        }

        ModelTabeleKlijenti mt = (ModelTabeleKlijenti) tblKlijenti.getModel();
        Klijent k = mt.getKlijent(selectedRow);

        int potvrda = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da obrisete klijenta?", "Potvda brisanja", JOptionPane.YES_NO_OPTION);

        if (potvrda != JOptionPane.YES_OPTION) {
            return;
        }

        try {
            Request zahtev = new Request();
            zahtev.setData(k);
            zahtev.setOperation(Operacije.DELETE_KLIJENT);

            Komunikacija.getInstance().posaljiZahtev(zahtev);
            Response odgovor = Komunikacija.getInstance().primiOdgovor();

            boolean uspesno = (boolean) odgovor.getResult();

            if (uspesno) {
                JOptionPane.showMessageDialog(this, "Klijent je uspesno obrisan!");
                sviKlijenti.removeIf(kl -> kl.getKlijentID() == k.getKlijentID());
                filtrirajKlijente();
            } else {
                JOptionPane.showMessageDialog(this, "Greska prilikom brisanja!", "Greska!", JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Greska prilikom brisanja klijenta!", "Greska!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

