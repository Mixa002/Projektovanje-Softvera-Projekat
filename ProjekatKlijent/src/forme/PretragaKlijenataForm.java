    private void ucitajKlijente() {
        try {

            Request zahtev = new Request();
            zahtev.setOperation(Operacije.GET_ALL_KLIJENT);
            Komunikacija.getInstance().posaljiZahtev(zahtev);
            Response odgovor = Komunikacija.getInstance().primiOdgovor();
            sviKlijenti = (ArrayList<Klijent>) odgovor.getResult();
            ModelTabeleKlijenti mt = new ModelTabeleKlijenti(sviKlijenti);
            tblKlijenti.setModel(mt);
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Greska prilikom ucitavanja klijenata!", "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void filtrirajKlijente() {
        if (sviKlijenti == null) {
            return;
        }

        String tekst = txtIme.getText().toLowerCase();

        if (tekst.isEmpty()) {
            tblKlijenti.setModel(new ModelTabeleKlijenti(sviKlijenti));
            return;
        }

        ArrayList<Klijent> filtrirani = new ArrayList<>();
        for (Klijent klijent : sviKlijenti) {
            String ime = klijent.getIme() != null ? klijent.getIme().toLowerCase() : "";
            String prezime = klijent.getPrezime() != null ? klijent.getPrezime().toLowerCase() : "";
            String punoIme = (ime + " " + prezime).trim();

            if (punoIme.contains(tekst)) {
                filtrirani.add(klijent);
            }
        }
        ModelTabeleKlijenti mt = new ModelTabeleKlijenti(filtrirani);
        tblKlijenti.setModel(mt);
    }
